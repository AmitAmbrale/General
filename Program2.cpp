#include<iostream>
using namespace std;
int main()
{
    int x=10;
    cout<<x<<endl;//10
    x=20;
    cout<<x<<endl;//20
    int y=x;
    x=30;
    cout<<x<<endl;//30
    cout<<y<<endl;//20
    cout<<x+y<<endl;//50
    cout<<x-y<<endl;//10
    cout<<x*y<<endl;//600
    cout<<x/y<<endl;//1


}