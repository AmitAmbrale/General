package Amit;

public class MaxMin
{
    public void Max(int i,int j)
    {
        if(i>j)
        {
            System.out.println("Maximum is : "+i);
        }
        else
        {
            System.out.println("Maximum is : "+j);
        }
    }
    public void Min(int i,int j)
    {
        if(i<j)
        {
            System.out.println("Minimum is : "+i);
        }
        else
        {
            System.out.println("Minimum is : "+j);
        }
    }
}